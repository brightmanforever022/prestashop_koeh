require SCRIPT_FOLDER . '/../../config/config.inc.php';

$csv = fopen('export_khl.csv', 'w+');
foreach( getRefs() as $ref){
    $prod = Db::getInstance()->getRow('
        SELECT * 
        FROM `'. _DB_PREFIX_ .'product` 
        WHERE `supplier_reference` LIKE "'. pSQL($ref) .'"
    ');
    if( !is_array($prod) || !count($prod) ){
        echo 'Product not found "'. $ref .'"<br>';
        continue;
    }
    
    $csvData = array(
        $prod['supplier_reference'],
        $prod['wholesale_price'],
        $prod['price']
    );
    
    fputcsv($csv, $csvData, ';', '"');
}
fclose($csv);


